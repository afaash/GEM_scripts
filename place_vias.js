var x = [];
var y = [];
var batch = 1;

var board_name = "ME0";
board_name = "GE21_M4";

var debug = false;

function Main() {
    CreateViaArray();

    var offset_x = 0;
    var offset_y = 400;
    if (board_name=="GE21_M4") {
        offset_x = -2000;
        offset_y = 620;
    }

    PCBServer.PreProcess
    var board = PCBServer.GetCurrentPCBBoard
    if(board == null){
        ShowMessage("No PCB or footprint editor activated.")
    } else {
        // ShowMessage("Origin is at ("+board.XOrigin+","+board.YOrigin+")")
        // find net
        var Iterator = board.BoardIterator_Create;
        Iterator.AddFilter_ObjectSet(MkSet(eNetObject));
        Iterator.AddFilter_LayerSet(AllLayers);
        Iterator.AddFilter_Method(eProcessAll);
        var net = Iterator.FirstPCBObject;

        for (var ivia=0; ivia<(x.length); ivia++){
            if (debug && ivia%20!=0) continue;
            if (board_name=="ME0") {
                if (batch==1 && ivia>900) continue;
                else if (batch==2 && (ivia<=900 || ivia>1700)) continue;
                else if (batch==3 && (ivia<=1700 || ivia>2600)) continue;
                else if (batch==4 && (ivia<=2600)) continue;
            }

            var via       = PCBServer.PCBObjectFactory(eViaObject, eNoDimension, eCreate_Default)
            via.X         = MMsToCoord(x[ivia]+offset_x)
            via.Y         = MMsToCoord(y[ivia]+offset_y)
            via.Size      = MMsToCoord(0.6)
            via.HoleSize  = MMsToCoord(0.3)
            via.LowLayer  = eBottomLayer
            via.HighLayer = eTopLayer

            var conn = Math.floor(ivia/128)+1;
            var pin = ivia%128 + 1;
            if (board_name=="GE21_M4" && conn<7) pin = 129-pin;
            var net_name = 'C'+conn+'_V'+pin;
            net = Iterator.FirstPCBObject;
            while (net != null && net.Name != net_name) {
                net = Iterator.NextPCBObject;
            }
            via.Net       = net;

            board.AddPCBObject(via)
            PCBServer.SendMessageToRobots(board.I_ObjectAddress, c_Broadcast, 
                PCBM_BoardRegisteration, via.I_ObjectAddress)
        }
        board.BoardIterator_Destroy(Iterator);
        Client.SendMessage("PCB:Zoom", "Action=Redraw" , 255, Client.CurrentView)
    }
    PCBServer.PostProcess
}

