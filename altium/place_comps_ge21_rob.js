var x = [];
var y = [];
var conn_x = [];
var conn_y = [];
var conn_ang = [];

function Main() {
    CreateConnectorArray();
    CreateViaArray();

    var offset_x = -1300;
    var offset_y = 400;

    PCBServer.PreProcess
    var board = PCBServer.GetCurrentPCBBoard
    if(board == null){
        ShowMessage("No PCB or footprint editor activated.")
    } else {
        // ShowMessage("Origin is at ("+board.XOrigin+","+board.YOrigin+")")
        for (var ivia=0; ivia<(x.length); ivia++){

            var via       = PCBServer.PCBObjectFactory(eViaObject, eNoDimension, eCreate_Default)
            via.X         = MMsToCoord(x[ivia]+offset_x)
            via.Y         = MMsToCoord(y[ivia]+offset_y)
            via.Size      = MMsToCoord(0.6)
            via.HoleSize  = MMsToCoord(0.35)
            via.LowLayer  = eBottomLayer
            via.HighLayer = eTopLayer

            board.AddPCBObject(via)
            PCBServer.SendMessageToRobots(board.I_ObjectAddress, c_Broadcast, 
                PCBM_BoardRegisteration, via.I_ObjectAddress)
        }

        // find and move connectors
        var comp_iterator = board.BoardIterator_Create;
        comp_iterator.AddFilter_ObjectSet(MkSet(eComponentObject));
        comp_iterator.AddFilter_LayerSet(AllLayers);
        comp_iterator.AddFilter_Method(eProcessAll);
        var conn = comp_iterator.FirstPCBObject;
        if (conn==null) {
           ShowMessage("No components found!")
           return;
        } 
        var i=0;
        while (conn != null) {
            PCBServer.SendMessageToRobots(conn.I_ObjectAddress, c_Broadcast, 
                PCBM_BeginModify , c_NoEventData);
            // ShowMessage("Found component "+i+" at ("+CoordToMMs(conn.X)+", "+CoordToMMs(conn.Y)+")");
            conn.MoveToXY(MMsToCoord(conn_x[i]+offset_x), MMsToCoord(conn_y[i]+offset_y));
            conn.RotateBy(conn_ang[i]);
            // ShowMessage("Moved "+i+" to ("+CoordToMMs(conn.X)+", "+CoordToMMs(conn.Y)+")");
            PCBServer.SendMessageToRobots(conn.I_ObjectAddress, c_Broadcast, 
                PCBM_EndModify , c_NoEventData);

            conn = comp_iterator.NextPCBObject;
            i++;
            if (i>conn_x.length) {
                ShowMessage("More components found on PCB than available coordinates!");
                break;
            }
        }
        board.BoardIterator_Destroy(comp_iterator);

        Client.SendMessage("PCB:Zoom", "Action=Redraw" , 255, Client.CurrentView)
    }
    PCBServer.PostProcess
}
